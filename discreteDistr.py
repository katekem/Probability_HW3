# Распределение Пуассона (частный случай биномиального распредедления).

def poisson(n, m, lyambda):
    # где n - количество испытаний, m - сколько раз произойдёт событие А,
    # lyambda - среднее количество наступления события 
    # за опр. единицу измерений (p * n-измерений)
    result = round((pow(lyambda, m) / math.factorial(m)) * pow(math.e, -lyambda), 5)
    return result