#-------------------------БИНОМИАЛЬНОЕ РАСПРЕДЕЛЕНИЕ-------------------------

# Когда используем: вероятность наступления событий велика, 
# число испытаний не очень большое.

import math

def binomial(n, k, p):
    # где n - количество испытаний, k - сколько раз произойдёт событие А, 
    # p - вероятность события А
    c = math.factorial(n) // (math.factorial(k) * math.factorial(n - k)) 
    # число способов выбрать k из n
    result = c * pow(p, k) * pow((1 - p), (n - k))
    return result


